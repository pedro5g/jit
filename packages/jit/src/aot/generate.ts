import { mkdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { emitCloneSource } from "../compiler/clone.js";
import { emitCodec } from "../compiler/codec/emit-codec.js";
import { emitEqualSource } from "../compiler/equal.js";
import { emitMaskSource } from "../compiler/mask.js";
import { emitSanitizeSource, sanitizeChainBindings } from "../compiler/sanitize.js";
import { emitSerialize } from "../compiler/serialize/emit-serialize.js";
import { emitValidator } from "../compiler/validate/emit-validate.js";
import type * as ATS from "../core/ats/index.js";
import type { SchemaInput } from "../core/builder/index.js";
import { unwrapSchema } from "../core/builder/index.js";
import { emitTypeScriptType } from "./emit-type.js";

/** One schema operation skipped by the generator and why. */
export interface SkippedOperation {
  readonly schema: string;
  readonly operation: string;
  readonly reason: string;
}

export interface GenerateOptions {
  /** Exported name → schema (or builder) to generate for. */
  readonly schemas: Readonly<Record<string, SchemaInput>>;
  /** Output directory; created if missing (e.g. `node_modules/@jit/generated`). */
  readonly outDir: string;
  /** Generated package name; defaults to `@jit/generated`. */
  readonly packageName?: string;
}

export interface GenerateResult {
  readonly files: readonly string[];
  readonly skipped: readonly SkippedOperation[];
}

/**
 * Prisma-style AOT generation: turns schemas into plain, optimized `.js`
 * plus `.d.ts`, written to a directory the app imports directly. The
 * generated module is fully self-contained — the few tiny runtime helpers
 * a strategy may need (validation error class, keyed-index cache) are
 * inlined into the file, so it has zero imports: no engine weight, no
 * module graph to load on cold start, CSP-safe.
 *
 * Each schema becomes a namespace constant (`export const User = { is,
 * parse, safeParse, equal, clone, stringify, mask, sanitize, codec }`);
 * operations whose bindings cannot be serialized (user callbacks in
 * refine/transform/default) are skipped and reported.
 */
export function generate(options: GenerateOptions): GenerateResult {
  const packageName = options.packageName ?? "@jit/generated";
  const skipped: SkippedOperation[] = [];
  const js: string[] = [];
  const dts: string[] = [];
  let needsRuntimeGetIndex = false;
  let needsValidationError = false;

  js.push("// Generated by jit — do not edit.");
  dts.push("// Generated by jit — do not edit.");

  for (const name of Object.keys(options.schemas)) {
    const schema = unwrapSchema(options.schemas[name] as SchemaInput<ATS.AnyTypeSchema>);
    const operations: string[] = [];
    const operationTypes: string[] = [];
    const valueType = emitTypeScriptType(schema);

    dts.push(`export type ${name} = ${valueType};`);

    // is / parse / safeParse
    const validator = tryEmit(name, "validator", skipped, () => emitValidator(schema));

    if (validator) {
      const inlined = inlineBindings(validator.bindings.names, validator.bindings.values);

      if (inlined === undefined) {
        skipped.push({
          schema: name,
          operation: "validator",
          reason: "refine/transform/default callbacks cannot be serialized ahead of time",
        });
      } else {
        needsValidationError = true;
        js.push(`const ${name}_validator = (() => {`);
        js.push(...inlined.map((line) => `  ${line}`));
        js.push(...indentBlock(validator.source));
        js.push("})();");
        operations.push(
          `is: ${name}_validator.is`,
          `safeParse: ${name}_validator.safeParse`,
          `parse: (value) => { const r = ${name}_validator.safeParse(value); if (r.success) return r.data; throw new JITValidationError(r.issues); }`
        );
        operationTypes.push(
          `readonly is: (value: unknown) => value is ${name}`,
          `readonly safeParse: (value: unknown) => { readonly success: true; readonly data: ${name} } | { readonly success: false; readonly issues: readonly { readonly path: string; readonly code: string; readonly expected: string; readonly message: string; readonly received?: string }[] }`,
          `readonly parse: (value: unknown) => ${name}`
        );
      }
    }

    // equal
    const equalSource = tryEmit(name, "equal", skipped, () => emitEqualSource(schema));

    if (equalSource) {
      if (equalSource.includes("__hash")) {
        skipped.push({ schema: name, operation: "equal", reason: "hash short-circuit hints require runtime hashing" });
      } else if (equalSource.includes("__getIndex")) {
        needsRuntimeGetIndex = true;
        js.push(`const ${name}_equal = (${equalSource});`);
        operations.push(`equal: ${name}_equal`);
        operationTypes.push(`readonly equal: (left: ${name}, right: ${name}) => boolean`);
      } else {
        js.push(`const ${name}_equal = (${equalSource});`);
        operations.push(`equal: ${name}_equal`);
        operationTypes.push(`readonly equal: (left: ${name}, right: ${name}) => boolean`);
      }
    }

    // clone
    const cloneSource = tryEmit(name, "clone", skipped, () => emitCloneSource(schema));

    if (cloneSource) {
      js.push(`const ${name}_clone = (${cloneSource});`);
      operations.push(`clone: ${name}_clone`);
      operationTypes.push(`readonly clone: (value: ${name}) => ${name}`);
    }

    // stringify
    const serializeSource = tryEmit(name, "stringify", skipped, () => emitSerialize(schema));

    if (serializeSource) {
      js.push(`const ${name}_stringify = (${serializeSource});`);
      operations.push(`stringify: ${name}_stringify`);
      operationTypes.push(`readonly stringify: (value: ${name}) => string`);
    }

    // mask
    const maskSource = tryEmit(name, "mask", skipped, () => emitMaskSource(schema));

    if (maskSource) {
      js.push(`const ${name}_mask = (${maskSource});`);
      operations.push(`mask: ${name}_mask`);
      operationTypes.push(`readonly mask: (value: ${name}) => ${name}`);
    }

    // sanitize
    const sanitizeSource = tryEmit(name, "sanitize", skipped, () => emitSanitizeSource(schema));

    if (sanitizeSource) {
      const regexConsts = sanitizeChainBindings.names.map(
        (bindingName, position) => `const ${bindingName} = ${String(sanitizeChainBindings.values[position])};`
      );

      js.push(`const ${name}_sanitize = (() => {`);
      js.push(...regexConsts.map((line) => `  ${line}`));
      js.push(...indentBlock(`return (${sanitizeSource});`));
      js.push("})();");
      operations.push(`sanitize: ${name}_sanitize`);
      operationTypes.push(`readonly sanitize: (value: ${name}) => ${name}`);
    }

    // codec
    const codec = tryEmit(name, "codec", skipped, () => emitCodec(schema));

    if (codec) {
      const inlined = inlineCodecBindings(codec.bindingNames, codec.bindingValues);

      if (inlined === undefined) {
        skipped.push({ schema: name, operation: "codec", reason: "codec bindings cannot be serialized" });
      } else {
        js.push(`const ${name}_codec = (() => {`);
        js.push(...inlined.map((line) => `  ${line}`));
        js.push(...indentBlock(codec.source));
        js.push("})();");
        operations.push(`codec: ${name}_codec`);
        operationTypes.push(
          `readonly codec: { readonly encode: (value: ${name}) => Uint8Array; readonly decode: (bytes: Uint8Array | ArrayBuffer) => ${name} }`
        );
      }
    }

    js.push(`export const ${name} = Object.freeze({`);
    js.push(...operations.map((operation) => `  ${operation},`));
    js.push("});");
    js.push("");

    dts.push(`export declare const ${name}: {`);
    dts.push(...operationTypes.map((type) => `  ${type};`));
    dts.push("};");
    dts.push("");
  }

  // Inlined runtime helpers keep the module import-free: nothing from the
  // engine is loaded in production, and cold start pays only this file.
  const helpers: string[] = [];

  if (needsValidationError) {
    helpers.push(
      "class JITValidationError extends Error {",
      "  constructor(issues) {",
      "    const first = issues[0];",
      '    super(first ? (first.path ? first.path + ": " : "") + first.message : "validation failed");',
      '    this.name = "JITValidationError";',
      '    this.code = "VALIDATION_FAILED";',
      "    this.issues = issues;",
      "  }",
      "}"
    );
  }
  if (needsRuntimeGetIndex) {
    helpers.push(
      "const __indexCache = new WeakMap();",
      "function __getIndex(items, key) {",
      "  const cached = __indexCache.get(items);",
      "  if (cached !== undefined && cached.key === key) return cached.map;",
      "  const map = new Map();",
      "  for (let i = 0, len = items.length; i < len; i++) map.set(items[i][key], items[i]);",
      "  __indexCache.set(items, { key: key, map: map });",
      "  return map;",
      "}"
    );
  }
  if (helpers.length > 0) js.splice(1, 0, ...helpers);

  mkdirSync(options.outDir, { recursive: true });

  const files = [
    writeFile(options.outDir, "index.js", `${js.join("\n")}\n`),
    writeFile(options.outDir, "index.d.ts", `${dts.join("\n")}\n`),
    writeFile(
      options.outDir,
      "package.json",
      `${JSON.stringify(
        {
          name: packageName,
          version: "0.0.0",
          type: "module",
          main: "./index.js",
          types: "./index.d.ts",
          sideEffects: false,
        },
        null,
        2
      )}\n`
    ),
  ];

  return { files, skipped };
}

function tryEmit<TValue>(
  schema: string,
  operation: string,
  skipped: SkippedOperation[],
  emit: () => TValue
): TValue | undefined {
  try {
    return emit();
  } catch (error) {
    skipped.push({ schema, operation, reason: error instanceof Error ? error.message : String(error) });
    return undefined;
  }
}

/**
 * Serializes validator bindings into const declarations. Returns undefined
 * when any value is a function or otherwise not representable as source.
 */
function inlineBindings(names: readonly string[], values: readonly unknown[]): string[] | undefined {
  const lines: string[] = [];

  for (let index = 0; index < names.length; index++) {
    const value = values[index];
    const literal = serializeBindingValue(value);

    if (literal === undefined) return undefined;

    lines.push(`const ${names[index]} = ${literal};`);
  }

  return lines;
}

function inlineCodecBindings(names: readonly string[], values: readonly unknown[]): string[] | undefined {
  const lines: string[] = [];

  for (let index = 0; index < names.length; index++) {
    const name = names[index];
    const value = values[index];

    if (name === "__enc") {
      lines.push("const __enc = new TextEncoder();");
      continue;
    }

    if (name === "__dec") {
      lines.push("const __dec = new TextDecoder();");
      continue;
    }

    const literal = serializeBindingValue(value);

    if (literal === undefined) return undefined;

    lines.push(`const ${name} = ${literal};`);
  }

  return lines;
}

function serializeBindingValue(value: unknown): string | undefined {
  if (value instanceof RegExp) return String(value);
  if (value === null || typeof value === "string" || typeof value === "number" || typeof value === "boolean") {
    return JSON.stringify(value);
  }
  if (value === undefined) return "undefined";
  if (Array.isArray(value)) {
    const parts = value.map(serializeBindingValue);

    if (parts.some((part) => part === undefined)) return undefined;
    return `[${parts.join(", ")}]`;
  }

  return undefined;
}

function indentBlock(source: string): string[] {
  return source.split("\n").map((line) => (line.length > 0 ? `  ${line}` : line));
}

function writeFile(dir: string, name: string, content: string): string {
  const path = join(dir, name);

  writeFileSync(path, content);
  return path;
}
